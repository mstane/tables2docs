package sm.t2d.transdata.config;

public class InputConfig {

    private EntityConfig mappings;
    private ParamsConfig params;

    public EntityConfig getMappings() {
        return mappings;
    }

    public void setMappings(EntityConfig mappings) {
        this.mappings = mappings;
    }

    public ParamsConfig getParams() {
        return params;
    }

    public void setParams(ParamsConfig params) {
        this.params = params;
    }


}
